    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Funcionario f = new Funcionario();
        
        f.setNome(fNome.getText());
        f.setDataContratacao(LocalDate.parse(fDataContatacao.getText()));
        f.setSalario(Float.parseFloat(fSalario.getText()));
        f.setTelefone(Integer.parseInt(fTelefone.getText()));
        f.setCargo(String.valueOf(cargoComboBox.getSelectedItem()));
         Medico m = new Medico();
        if(f.getCargo().equals("Médico")){
           
            m.setEspecialidade(String.valueOf(comboEspecialidade.getSelectedItem()));
            m.setNumeroDoMedico(mNumero.getText());
            m.setDiaDisponivel(String.valueOf(comboDias.getSelectedItem()));
        }
       Cfuncionario cadastrar = new Cfuncionario();
       cadastrar.salvar(f, m);
    }//GEN-LAST:event_jButton2ActionPerformed

