    private void cargoComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargoComboBoxActionPerformed
        // TODO add your handling code here:
        String selectedRole = (String) cargoComboBox.getSelectedItem();
        if("Médico".equals(selectedRole)){
            comboEspecialidade.setEnabled(true);
            comboDias.setEnabled(true);
            mNumero.setEnabled(true);
        }else{
            comboEspecialidade.setEnabled(false);
            comboDias.setEnabled(false);
            mNumero.setEnabled(false);
        }
    }//GEN-LAST:event_cargoComboBoxActionPerformed

